a = int(input("Введите переменную 1: "))

b = int(input("Введите переменную 2: "))

c = int(input("Введите переменную 3: "))

print( "сумма: ", a + b + c)
print( "вычитание: ", a - b - c)
print( "деление: ", a // b // c)
print( "умножение: ", a * b * c)
print( "остаток при деление: ", a % b % c)
